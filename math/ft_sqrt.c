#include <stdint.h>
#include <stdio.h>

uint64_t	ft_sqrt(uint64_t nb)
{
	uint64_t	i;

	i = 1;
	while ((i * i) <= nb && i <= 4294967296)
	{
		if ((i * i) == nb)
			return (i);
		i++;
	}
	return (0);
}
